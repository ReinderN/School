from math import factorial, sqrt

print("Nul is", 4+4-4-4)
print("Één is", (4+4)/(4+4))
print("Twee is", 4/4+4/4)
print("Drie is", (4+4+4)/4)
print("Vier is", (4*(4/4)**4))
print("Vijf is", (4*4+4)/4)
print("Zes is", (4+4)/4+4)
print("Zeven is", 4+4-4/4)
print("Acht is", 4*4/4+4)
print("Negen is", 4+4+4/4)
print("Tien is", 4*4-4-sqrt(4))
print("Elf is", (factorial(4)+factorial(4)-4)/4)
print("Twaalf is", 4+4+sqrt(4)+sqrt(4))
print("Dertien is", (factorial(4)+factorial(4)+4)/4)
print("Veertien is", 4+4+4+sqrt(4))
print("Vijftien is", 4*4-4/4)
print("Zestien is", 4*4*4/4)
print("Zeventien is", 4*4+4/4)
print("Achtien is", 4*sqrt(4)*sqrt(4)+sqrt(4))
print("Negentien is", factorial(4)-4-4/4)
print("Twintig is", 4*sqrt(4)*sqrt(4)+4)
